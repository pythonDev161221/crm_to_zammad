function getCurrentLang() {
  return localStorage.getItem('lang') || 'en';
}

window.setLang = function(lang) {
  localStorage.setItem('lang', lang);
  applyTranslations();
  // Re-render profile if currently on profile screen
  const active = document.querySelector('.screen.active');
  if (active && active.id === 'screen-profile' && typeof window.showProfile === 'function') {
    window.showProfile();
  }
};

window.t = function(key, vars) {
  const lang = getCurrentLang();
  const dict = TRANSLATIONS[lang] || TRANSLATIONS['en'];
  let str = dict[key] !== undefined ? dict[key] : (TRANSLATIONS['en'][key] !== undefined ? TRANSLATIONS['en'][key] : key);
  if (vars) {
    Object.keys(vars).forEach(k => {
      str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), vars[k]);
    });
  }
  return str;
};

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  // Highlight active language button
  ['en', 'ru', 'ky'].forEach(l => {
    const btn = document.getElementById('lang-btn-' + l);
    if (btn) btn.style.opacity = l === getCurrentLang() ? '1' : '0.5';
  });
}

window.initLang = function() {
  if (!localStorage.getItem('lang')) {
    const code = window.Telegram?.WebApp?.initDataUnsafe?.user?.language_code || '';
    if (code === 'ru') {
      localStorage.setItem('lang', 'ru');
    } else if (code === 'ky' || code === 'kg') {
      localStorage.setItem('lang', 'ky');
    } else {
      localStorage.setItem('lang', 'en');
    }
  }
  applyTranslations();
};
