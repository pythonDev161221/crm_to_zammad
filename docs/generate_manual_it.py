from docx.enum.text import WD_ALIGN_PARAGRAPH


def add_heading(doc, text, level=1):
    p = doc.add_heading(text, level=level)
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    return p


def add_para(doc, text, bold=False, italic=False):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = bold
    run.italic = italic
    return p


def add_step(doc, text):
    p = doc.add_paragraph(style='List Number')
    p.paragraph_format.left_indent = Inches(0.3)
    p.add_run(text)
    return p


def add_bullet(doc, text, indent=0.3):
    p = doc.add_paragraph(style='List Bullet')
    p.paragraph_format.left_indent = Inches(indent)
    p.add_run(text)
    return p


def add_note(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Inches(0.3)
    run = p.add_run('Примечание: ')
    run.bold = True
    run.font.color.rgb = RGBColor(0x8B, 0x44, 0x00)
    run2 = p.add_run(text)
    run2.italic = True
    run2.font.color.rgb = RGBColor(0x8B, 0x44, 0x00)
    return p


def add_warning(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Inches(0.3)
    run = p.add_run('Важно: ')
    run.bold = True
    run.font.color.rgb = RGBColor(0xC0, 0x00, 0x00)
    run2 = p.add_run(text)
    run2.font.color.rgb = RGBColor(0xC0, 0x00, 0x00)
    return p


def add_screenshot(doc, description):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Inches(0.3)
    run = p.add_run(f'[СКРИНШОТ: {description}]')
    run.bold = True
    run.font.color.rgb = RGBColor(0x00, 0x70, 0xC0)
    return p


def add_page_break(doc):
    doc.add_page_break()


